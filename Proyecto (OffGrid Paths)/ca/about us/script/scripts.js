function changeLanguage(lang) {
    if (lang === 'en') {
        window.location.href = '../../../en/about us/aboutus.html';
    } else if (lang === 'es') {
        window.location.href = '../../../about us/aboutus.html';
    } else if (lang === 'ca') {
        window.location.href = '../aboutus.html';
    }
}